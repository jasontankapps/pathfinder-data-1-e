export const $KnownProps = {};

$All.forEach(([link, object]) => {
	// Create master list of links
	$KnownProps[link] = {};
	Object.keys(object).forEach(key => ($KnownProps[link][key] = true));
});

// Ex:
//    $KnownProps.race.ratfolk = true
//    $KnownProps.race.rat = undefined

const isALink = (protocol, link) => {
	if($KnownProps[protocol]) {
		return $KnownProps[protocol][link] || false;
	} else {
		return undefined;
	}
};

export default isALink;
