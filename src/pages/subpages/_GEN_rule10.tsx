<p>If you're having trouble visualizing the size and population of a village, town, or city, compare it to real-life locations and gauge accordingly. For example, at its height at the end of the 2nd Century, Rome boasted over a million people (although census records were sketchy - some report nearly 10 times that number!). During the 14th Century, Rome's population had declined drastically to around 50,000 people. Although these numbers might not seem particularly impressive compared to modern cities, Rome was considered massive and teeming with people.</p>
